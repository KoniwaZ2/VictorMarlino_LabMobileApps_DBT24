class Products {
  final int id;
  final String name;
  final String image;
  final int price;
  final int stock;
  final String size;
  final String color;
  final int discount;

  Products({
    required this.id,
    required this.name,
    required this.image,
    required this.price,
    required this.stock,
    required this.size,
    required this.color,
    required this.discount,
  });
}
